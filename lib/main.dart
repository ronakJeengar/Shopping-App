      title: appname,
      theme: ThemeData(
          scaffoldBackgroundColor: Colors.transparent,
          appBarTheme: const AppBarTheme(
              iconTheme: IconThemeData(
                color: darkFontGrey, // Change the color here
              ),
              backgroundColor: Colors.transparent),
          fontFamily: regular),
      home: const SplashScreen(),
